            String sql="INSERT into Account(Username,Name,Password,Sec_Q,Answer) values (?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1,txtUser.getText());
            pst.setString(2,txtName.getText());
